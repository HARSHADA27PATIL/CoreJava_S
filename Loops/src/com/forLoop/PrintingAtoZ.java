package com.forLoop;

public class PrintingAtoZ {

	public static void main(String[] args) {
		// ‚¶Å	Write a Java program to print all alphabets from a to z. - using  for loop
		for(char ch='a';ch<='z';ch++)
		{
			System.out.println(ch);
		}
		for(char ch='A';ch<='Z';ch++)
		{
			System.out.println(ch);
		}
	}

}
